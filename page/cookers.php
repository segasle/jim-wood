<h1 class="text-center h2 fatty">
    Кухня-модерн в коттедж
</h1>

<div class="block_text block-center">
    <div class="block_text-descriptions">
        <p><span class="tab fatty">Современный стиль с элементами модерна</span>
            – это то, что нужно для любителей стильных и функциональных кухонь. Модерн – один из самых сложных
            современных стилей, ведь он вобрал в себя черты и классики, и минимализма, и хай-тек. Кухня в таком дизайне
            отличается, в первую очередь, лаконичностью и удобством. Как правило, она имеет гладкие поверхности в
            сочетании с древесными текстурами, оборудована встроенной современной техникой и выдержана в монохромной
            гамме приглушенных холодных оттенков. Изюминкой является то, что такой дизайн допускает аутентичные элементы
            декора: растительность в горшках, искусственные фрукты, имитация больших столовых приборов на стене. Проект,
            представленный ниже, не оставит равнодушными всех любителей стильного и функционального интерьера!

        </p>
