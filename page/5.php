<h1 class="text-center h2 fatty">Небольшая кухня в стиле хай-тек</h1>

<div class="block_text block-center">
    <div class="block_text-descriptions">
        <p><span class="fatty">Хай-тек</span> относится к категории современных стилей интерьера. Это один из самых молодых стилей, ведь он зародился в начале XXI века. Дословно «хай-тек» переводится с английского как «высокие технологии». Именно в этом заключается основная фишка данного стиля. Применяются только современные технологические материалы, которые значительно облегчают процесс содержания кухни в чистоте, при этом уклон идет на функциональность кухонного гарнитура и рациональное использование «рабочего» пространства. Например, зона сушки посуды, а также столовые приборы прячутся внутри шкафчиков. Зона для приготовления пищи, как самое функциональное место на кухне, обычно располагается в ближайшем доступе до техники, посуды, раковины. Таким образом происходит экономия не только пространства, но и времени. Кстати, техника, как правило, скрывается за фасадом мебели. И еще одна отличительная деталь состоит в том, что дизайнеры стараются не использовать декоративные элементы, которые обычно зрительно «утяжеляют» интерьер. В общем, хай-тек – это стильно, удобно и функционально. Убедитесь в этом сами, посмотрев фотографии нашего проекта!</p>
    </div>
