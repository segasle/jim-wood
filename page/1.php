<h1 class="text-center h2 fatty">
    Большая кухня в английском стиле</h1>

<div class="block_text block-center">
    <div class="block_text-descriptions">
        <p><span class="fatty"> Английский стиль</span> – это весьма серьезное направление в мире дизайна, зародившееся
            очень давно,
            но при этом никогда не имевшее популярность в России. И вот сейчас, когда традиционно приевшийся итальянский
            стиль постепенно надоедает, люди начинают обустраивать кухни в новом, свежем стиле. Наша фирма “Jim Wood” не
            отстает от этой тенденции и производит мебельные фасады в английском стиле. Ведь английский стиль в
            интерьере- это всегда элегантно и респектабельно. Для того чтобы внести ясность, рассмотрим основные
            особенности кухонь в английском стиле и сделаем вывод о том, какие для них должны быть фасады.
        </p>
        <ol class="number">
            <li><span class="fatty">Строгость и симметричность</span>
                <ul>
                    <li>Традиционные английские интерьеры не имеют кривых линий, они просты и изящны, так как англичане
                        - это очень правильный народ, любящий порядок и отсутствие лишних деталей.
                    </li>
                </ul>
            </li>
            <li><span class="fatty">Элементы античной культуры.</span>
                <ul>
                    <li>
                        Британский народ очень любит разбавлять свой строгий интерьер декоративными элементами
                        различного вида: Пилястры, Колонны, Арки, Балюстрады. Ведь каждый человек хочет добавить в свой
                        интерьер капельку изысканности.
                    </li>
                </ul>
            </li>
            <li><span class="fatty"> Плотность планировки.</span>
                <ul>
                    <li>Мебель в английском стиле спроектирована так, чтобы максимально заполнить пустые места, потому
                        что британский менталитет таков, что они хотят использовать свои ресурсы максимально эффективно,
                        что, разумеется, сказалась и в их домах.
                    </li>
                </ul>
            </li>
            <li><span class="fatty">Дорогие материалы.</span>
                <ul>
                    <li>Англичане никогда не скупились на материалы для обустройства своего дома, выбирая только лучшее.
                        Это, конечно, подразумевает под собой Фасады из Массива дерева. К счастью, технологии не стоят
                        на месте и для снижения стоимости фасадов, наша фирма может предложить вам Фасады, покрытые
                        шпоном, что визуально будет выглядеть абсолютно так же элегантно.
                    </li>
                </ul>
            </li>
        </ol>
    </div>
</div>
