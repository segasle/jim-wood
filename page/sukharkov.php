<h1 class="text-center fatty h2">
    Комплексная мебелировка квартиры в современном стиле
</h1>

<div class="block_text block-center">
    <div class="block_text-descriptions">
        <p><span class="tab fatty">Современный стиль</span>
   создан для того, чтобы отдохнуть от поднадоевшей классики, от пафоса и «громоздкости», при этом не теряя уют и гармонию. Главная особенность современной кухни заключается в её практичности и функциональности. Она может быть выглядеть просто и лаконично, но иметь «навороченную» начинку. Внешний вид таких кухонь не подчинен каким-либо дизайнерским канонам оформления, он может сочетать в себе черты нескольких современных стилей: минимализма, модерна, хай-тек, скандинавского, и других.  Главное, чтобы интерьер выглядел стильно и создавал уютную атмосферу «невесомости» пространства. Обычно это ощущение достигается за счет того, что фасады имеют гладкую поверхность – никаких массивных выступающих линий, «тяжелого люкса» и прочих извращенных инсинуаций. Современный стиль, как и всё гениальное, прост. Дверцы открываются от обычного нажатия рукой, имеют либо интегрированные ручки, либо ручки простой геометрической формы.

        </p>
